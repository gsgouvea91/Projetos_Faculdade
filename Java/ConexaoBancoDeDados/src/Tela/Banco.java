package Tela;

public class Banco {
    public String banco;
    public double agencia;
    public String foneAg;
    public double conta;
    public String dtAbetura;
    public int nCheque;

    public String getBanco() {
        return banco;
    }

    public void setBanco(String banco) {
        this.banco = banco;
    }

    public double getAgencia() {
        return agencia;
    }

    public void setAgencia(double agencia) {
        this.agencia = agencia;
    }

    public String getFoneAg() {
        return foneAg;
    }

    public void setFoneAg(String foneAg) {
        this.foneAg = foneAg;
    }

    public double getConta() {
        return conta;
    }

    public void setConta(double conta) {
        this.conta = conta;
    }

    public String getDtAbetura() {
        return dtAbetura;
    }

    public void setDtAbetura(String dtAbetura) {
        this.dtAbetura = dtAbetura;
    }

    public int getnCheque() {
        return nCheque;
    }

    public void setnCheque(int nCheque) {
        this.nCheque = nCheque;
    }
    
    
}
