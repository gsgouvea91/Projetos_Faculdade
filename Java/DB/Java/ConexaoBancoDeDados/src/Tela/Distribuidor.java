package Tela;

public class Distribuidor {
    public String da;
    private String telCel;
    private String telRes;
    private String monitor;
    private String monitorPE;
    private String data;
    private String pDistrib;

    public String getDa() {
        return da;
    }
    
    public void setDa(String da) {
        this.da = da;
    }

    public String getTelCel() {
        return telCel;
    }

    public void setTelCel(String telCel) {
        this.telCel = telCel;
    }

    public String getTelRes() {
        return telRes;
    }

    public void setTelRes(String telRes) {
        this.telRes = telRes;
    }

    public String getMonitor() {
        return monitor;
    }

    public void setMonitor(String monitor) {
        this.monitor = monitor;
    }

    public String getMonitorPE() {
        return monitorPE;
    }

    public void setMonitorPE(String monitorPE) {
        this.monitorPE = monitorPE;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getpDistrib() {
        return pDistrib;
    }

    public void setpDistrib(String pDistrib) {
        this.pDistrib = pDistrib;
    }
    
    
}
